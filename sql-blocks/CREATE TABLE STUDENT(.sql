CREATE TABLE STUDENTS(
    student_id NUMBER PRIMARY KEY,
    student_name VARCHAR2(40),
    student_address VARCHAR2(45),
    student_age NUMBER,
    joined_date DATE
);

CREATE SEQUENCE sys.STUDENTS_SEQ 
    INCREMENT BY 1 
    START WITH 1 
    NOCACHE 
    NOCYCLE;

DROP SEQUENCE sys.STUDENTS_SEQ;

SELECT SEQUENCE_OWNER, SEQUENCE_NAME FROM ALL_SEQUENCES WHERE SEQUENCE_NAME = 'STUDENTS_SEQ';

GRANT CREATE ANY SEQUENCE, SELECT ANY SEQUENCE TO system;
GRANT SELECT, ALTER ON sys.STUDENTS_SEQ TO system;

